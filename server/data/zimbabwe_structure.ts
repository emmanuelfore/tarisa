// Get all wards for a city
export function getWardsByCity(cityCode: string) {
    const cityMap: Record<string, keyof typeof ZIMBABWE_STRUCTURE.wards> = {
        'ZW-HA-CITY': 'harare',
        'ZW-BU-CITY': 'bulawayo',
        'ZW-MA-MUT': 'mutare',
        'ZW-MI-GWE': 'gweru'
    };

    const cityKey = cityMap[cityCode];
    return ZIMBABWE_STRUCTURE.wards[cityKey] || [];
}

// Get total wards count
export function getTotalWards() {
    return Object.values(ZIMBABWE_STRUCTURE.wards)
        .reduce((total, cityWards) => total + cityWards.length, 0);
}

// Get ward by number and city
export function getWard(cityCode: string, wardNumber: number) {
    const wards = getWardsByCity(cityCode);
    return wards.find(w => w.ward === wardNumber);
}

// Get all suburbs across all cities
export function getAllSuburbs() {
    const allSuburbs: string[] = [];

    Object.values(ZIMBABWE_STRUCTURE.wards).forEach(cityWards => {
        cityWards.forEach(ward => {
            allSuburbs.push(...ward.suburbs);
        });
    });

    return allSuburbs.sort();
}

// Search wards by suburb name
export function findWardBySuburb(suburbName: string) {
    for (const [city, wards] of Object.entries(ZIMBABWE_STRUCTURE.wards)) {
        for (const ward of wards) {
            if (ward.suburbs.some(s => s.toLowerCase().includes(suburbName.toLowerCase()))) {
                return { city, ward };
            }
        }
    }
    return null;
}

// Get department by category
export function getDepartmentByCategory(category: string) {
    for (const [key, dept] of Object.entries(ZIMBABWE_STRUCTURE.departments)) {
        if (dept.categories.includes(category)) {
            return { key, ...dept };
        }
    }
    return null;
}

// Get issue category details
export function getIssueCategory(code: string) {
    return ZIMBABWE_STRUCTURE.issue_categories.find(cat => cat.code === code);
}
